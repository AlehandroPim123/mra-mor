      <h2>Особенности и технологические тонкости  производства </h2>

      <p align="justify"><br />
      <strong>Шаг 1. Изготовление матрицы</strong></p>
      <p align="justify"><br />
      Матрица или форма – модель будущего изделия в натуральную  величину только наоборот, как бы зеркальное отражение в объеме. Сборка матрицы - это  сложная и кропотливая работа. Периметр изделия (формы) собирается из металлических  профилей квадратного  сечения. Для  радиусных и криволинейных форм на профиле делаются многочисленные разрезы по  внешней или внутренней стороне и радиусный сегмент аккуратно сгибается для достижения нужной кривизны. </p>
      <p align="justify"><br />
        Собранный периметр формы закрепляется на основании вибрационного  стола с идеально ровной, практически зеркальной поверхностью. Затем формируется  борт – кромка, из материала, напоминающего пластилин. Важно строго соблюдать  правильность геометрии, любая ошибка приведет к отбраковке изделия. </p>
