      <p><strong>Шаг 4. Шлифовка и полировка</strong> </p>
        <p>Но и это еще не все. Для получения фирменного, отменного  качества изделия шлифуются и полируются. Шлифовка происходит вручную, с водой,  постепенно, десятью различными номерами автомобильной шкурки. Убираются  все  малейшие неровности и шероховатости,  мельчайшие дефекты и поверхность доводится до совершенства.  После чего полируется до зеркального блеска.  Очень трудоемкий процесс, у опытного мастера шлифовка и полировка одной  столешницы, размером три квадратных метра - занимает два дня. Этот процесс  практически невозможно автоматизировать без ущерба качеству, а это недопустимо.</p>
